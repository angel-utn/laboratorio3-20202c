CREATE DATABASE MODELOPARCIAL2_PUNTO2

USE MODELOPARCIAL2_PUNTO2

CREATE TABLE ESPECIES(
 IDESPECIE BIGINT NOT NULL PRIMARY KEY,
 ESPECIE VARCHAR(50) NOT NULL,
)
GO
CREATE TABLE PARTICIPANTES(
 IDPARTICIPANTE BIGINT NOT NULL PRIMARY KEY,
 APELLIDO VARCHAR(50) NOT NULL,
 NOMBRE VARCHAR(50) NOT NULL,
 SEXO CHAR NOT NULL,
 FECHA_NACIMIENTO SMALLDATETIME NOT NULL
)
GO
CREATE TABLE PESCA(
 IDPESCA BIGINT NOT NULL PRIMARY KEY,
 IDPARTICIPANTE BIGINT NOT NULL FOREIGN KEY REFERENCES PARTICIPANTES(IDPARTICIPANTE),
 IDESPECIE BIGINT NULL FOREIGN KEY REFERENCES ESPECIES(IDESPECIE),
 PESO DECIMAL(10,2) NOT NULL,
 FECHA_HORA SMALLDATETIME NOT NULL
)

